package javafirstapplicationgit;
import java.util.*;
 
public class Model {
    int cuscini;
    String nomeMaterasso;
    String grandezza;
    int prezzo;

    public String getNomeMaterasso() {
        return nomeMaterasso;
    }

    public void setNomeMaterasso(String nomeMaterasso) {
        this.nomeMaterasso = nomeMaterasso;
    }

    public int getCuscini() {
        return cuscini;
    }

    public void setCuscini(int cuscini) {
        this.cuscini = cuscini;
    }
    
    public String getGrandezza() {
        return grandezza;
    }

    public void setGrandezza(String grandezza) {
        this.grandezza = grandezza;
    }

    public int getPrezzo() {
        return prezzo;
    }

    public void setPrezzo(int prezzo) {
        this.prezzo = prezzo;
    }
    
    
}

